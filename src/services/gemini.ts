import { GoogleGenAI } from "@google/genai";

const ai = new GoogleGenAI({
  apiKey: import.meta.env.VITE_GEMINI_API_KEY 
});

export const generateChatResponse = async (message: string) => {
  const prompt = `Ты — ИИ-ассистент ситуационного центра Алматы. Пользователь спрашивает: "${message}"
  
  Дай полезный, краткий ответ на русском языке. Будь профессиональным и helpful.
  Если вопрос о городе, используй контекст умного города Алматы.`;

  try {
    const response = await ai.models.generateContent({
      model: "gemini-1.5-flash",
      contents: prompt,
    });

    return response.text;
  } catch (error) {
    console.error("Chat Gemini Error:", error);
    return "Извините, произошла ошибка при обработке запроса.";
  }
};

